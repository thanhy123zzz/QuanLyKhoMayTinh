package com.khomaytinh.quanlykhomaytinh.Model;

public class ManHinh extends HangHoa{
    private String Model ;
    private String KetNoi ;
    private String TanSoQuet ;
    private String TiLeKhungHinh ;
    private String LoaiMH ;
    private String MauSacHienThi ;

    public String getModel() {
        return Model;
    }

    public void setModel(String model) {
        Model = model;
    }

    public String getKetNoi() {
        return KetNoi;
    }

    public void setKetNoi(String ketNoi) {
        KetNoi = ketNoi;
    }

    public String getTanSoQuet() {
        return TanSoQuet;
    }

    public void setTanSoQuet(String tanSoQuet) {
        TanSoQuet = tanSoQuet;
    }

    public String getTiLeKhungHinh() {
        return TiLeKhungHinh;
    }

    public void setTiLeKhungHinh(String tiLeKhungHinh) {
        TiLeKhungHinh = tiLeKhungHinh;
    }

    public String getLoaiMH() {
        return LoaiMH;
    }

    public void setLoaiMH(String loaiMH) {
        LoaiMH = loaiMH;
    }

    public String getMauSacHienThi() {
        return MauSacHienThi;
    }

    public void setMauSacHienThi(String mauSacHienThi) {
        MauSacHienThi = mauSacHienThi;
    }
}
