package com.khomaytinh.quanlykhomaytinh.Model;

public class TaiNghe extends HangHoa{
    private String Model;
    private String KetNoi;
    private String KieuTN;
    private String Led;
    private String Mau;
    private String TanSo;
    private String KhaNangCachAm;
    private String ChatLieu;

    public String getModel() {
        return Model;
    }

    public void setModel(String model) {
        Model = model;
    }

    public String getKetNoi() {
        return KetNoi;
    }

    public void setKetNoi(String ketNoi) {
        KetNoi = ketNoi;
    }

    public String getKieuTN() {
        return KieuTN;
    }

    public void setKieuTN(String kieuTN) {
        KieuTN = kieuTN;
    }

    public String getLed() {
        return Led;
    }

    public void setLed(String led) {
        Led = led;
    }

    public String getMau() {
        return Mau;
    }

    public void setMau(String mau) {
        Mau = mau;
    }

    public String getTanSo() {
        return TanSo;
    }

    public void setTanSo(String tanSo) {
        TanSo = tanSo;
    }

    public String getKhaNangCachAm() {
        return KhaNangCachAm;
    }

    public void setKhaNangCachAm(String khaNangCachAm) {
        KhaNangCachAm = khaNangCachAm;
    }

    public String getChatLieu() {
        return ChatLieu;
    }

    public void setChatLieu(String chatLieu) {
        ChatLieu = chatLieu;
    }
}
